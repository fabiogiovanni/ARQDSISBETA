package to;
public class CursoTO {
	
	private String cod, nome, numLab, regSoft, material, livro, dataIni, dataTer, horario;
	private int vagas;
	private double valor;
	
	public String getCod() {
		return cod;

	}

	public void setCod(String cod) {

		this.cod = cod;

	}

	public String getNome() {
		return nome;

	}

	public void setNome(String nome) {

		this.nome = nome;

	}

	public String getNumLab() {
		return numLab;

	}

	public void setNumLab(String numLab) {

		this.numLab = numLab;

	}

	public String getRegSoft() {
		return regSoft;

	}

	public void setRegSoft(String regSoft) {

		this.regSoft = regSoft;

	}

	public String getMaterial() {
		return material;

	}

	public void setMaterial(String material) {

		this.material = material;

	}

	public String getLivro() {
		return livro;

	}

	public void setLivro(String livro) {

		this.livro = livro;

	}

	public String getDataIni() {
		return dataIni;

	}

	public void setDataIni(String dataIni) {

		this.dataIni = dataIni;

	}

	public String getDataTer() {
		return dataTer;

	}

	public void setDataTer(String dataTer) {

		this.dataTer = dataTer;

	}

	public String getHorario() {
		return horario;

	}

	public void setHorario(String horario) {

		this.horario = horario;

	}

	public int getVagas() {
		return vagas;

	}

	public void setVagas(int vagas) {

		this.vagas = vagas;

	}

	public double getValor() {
		return valor;

	}

	public void setValor(double valor) {

		this.valor = valor;

	}


}